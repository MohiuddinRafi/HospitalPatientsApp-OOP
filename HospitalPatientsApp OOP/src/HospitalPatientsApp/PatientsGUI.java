    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        if (ordinaryPatientsRb.isSelected()) {
            //declare and create a new manager object m
            OrdinaryPatients o = new OrdinaryPatients();

            o.setName(nameTf.getText());
            o.setAge(ageTf.getText());
            o.setGuardNam(guardNamTf.getText());
            o.setPpsn(ppsnTf.getText());
            o.setSymptoms(symptomsTf.getText());
            o.setSymDuration(symDurationTf.getText());
            patients.add(o);

        } else if (injuredPatientsRb.isSelected()) {

            InjuredPatients i = new InjuredPatients();
            i.setName(nameTf.getText());
            i.setAge(ageTf.getText());
            i.setGuardNam(guardNamTf.getText());
            i.setPpsn(ppsnTf.getText());
            i.setInjurType(injurTypeTf.getText());
            i.setBloodP(bloodPTf.getText());
            //add object of arraylist
            patients.add(i);

        } else if (criticalPatientsRb.isSelected()) {

            CriticalPatients c = new CriticalPatients();
            c.setName(nameTf.getText());
            c.setAge(ageTf.getText());
            c.setGuardNam(guardNamTf.getText());
            c.setPpsn(ppsnTf.getText());
            c.setOperation(operationTf.getText());
            c.setConcent(concentTf.getText());

            patients.add(c);
        }
        clearFields();
        save();
    }//GEN-LAST:event_addBtnActionPerformed

