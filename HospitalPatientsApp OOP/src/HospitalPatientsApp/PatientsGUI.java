    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        // TODO add your handling code here:
        if (patients.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Sorry, there are no patient to remove");
        } else {
            String SearchTerm = ppsnTf.getText();
            boolean found = false;
            for (int i = 0; i < patients.size(); i++) {
                Patients p = patients.get(i);

                if (p.getPpsn().equalsIgnoreCase(SearchTerm)) {

                    patients.remove(p);
                    found = true;
                    break;

                }

            }
            if (!found) {
                JOptionPane.showMessageDialog(null, "No patient found with PPSN: " + SearchTerm);
            }

        }
        clearFields();
        save();
    }//GEN-LAST:event_removeBtnActionPerformed

